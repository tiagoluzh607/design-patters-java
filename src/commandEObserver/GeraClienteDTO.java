package commandEObserver;

public class GeraClienteDTO {
	
	private String nome;
	private String email;
	
	public GeraClienteDTO(String nome, String email) {
		super();
		this.nome = nome;
		this.email = email;
	}

	public String getNome() {
		return nome;
	}

	public String getEmail() {
		return email;
	}
	
	
	

}
